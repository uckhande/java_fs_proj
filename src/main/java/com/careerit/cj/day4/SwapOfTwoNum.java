package com.careerit.cj.day4;

public class SwapOfTwoNum {
    public static void main(String[] args) {
        int a =10;
        int b = 20;
        System.out.println("Before Swap a=" +a+ "and b= +b");
        int temp =a;
        a=b;
        b=temp;
        System.out.println("After swap a=" +a+ "and b=" +b);
    }
}
